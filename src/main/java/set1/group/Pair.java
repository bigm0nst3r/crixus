package set1.group;

/**
 * @author Sreejith S
 * @since Jan 18, 2015
 */
public class Pair {


  private int main;
  private int companion;


  public Pair(int main, int companion) {
    this.main = main;
    this.companion = companion;
  }

  public int getMain() {
    return main;
  }

  public void setMain(int main) {
    this.main = main;
  }

  public int getCompanion() {
    return companion;
  }

  public void setCompanion(int companion) {
    this.companion = companion;
  }
}
